#!/usr/bin/env ruby

class MapGrid
  def initialize(lines)
    @text_lines = lines
    @width = lines.first.size
    @height = lines.size
  end

  # given right and down, we continually move in those increments and count
  # trees hit. if we hit the right edge of the map, continue back wrapping
  # on the left side (cylinder). End when we hit the bottom.
  # returns: count of trees hit
  def trees_on_slope(right:, down:)
    x = 0
    y = 0
    count = 0
    loop do
      count += 1 if map(x, y) == "#"
      x += right
      y += down
      break if y >= @height
    end
    count
  end

  # top left is 0,0, moving right and down is positive.
  def map(x, y)
    return nil if y >= @height

    @text_lines[y][x % @width]
  end
end

if __FILE__ == $0
  lines = DATA.readlines(chomp: true)
  map = MapGrid.new(lines)
  part1 = map.trees_on_slope(right: 3, down: 1)
  puts "in part 1, going right 3 and down 1, we hit #{part1} trees."

  # part 2: do the same as above, but with 5 different slopes.
  # answer is the counts multiplied
  # Right 1, down 1.
  # Right 3, down 1. (This is the slope you already checked.)
  # Right 5, down 1.
  # Right 7, down 1.
  # Right 1, down 2.
  slopes = [
    [1, 1],
    [3, 1],
    [5, 1],
    [7, 1],
    [1, 2]
  ]
  part2 = slopes.map { |r, d| map.trees_on_slope(right: r, down: d) }.reduce(:*)
  puts "in part 2, the multiplied answer is #{part2}"
end

__END__
